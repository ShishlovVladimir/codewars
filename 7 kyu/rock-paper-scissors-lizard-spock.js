function rpsls(pl1, pl2) {
  if (pl1 === pl2) return `Draw!`;
  let arr = [`rock`, `lizard`, `spock`, `scissors`, `paper`];
  let n1 = arr.indexOf(pl1);
  let n2 = arr.indexOf(pl2);
  return (n1 + 1) % 5 === n2 || (n1 + 3) % 5 === n2
    ? `Player 1 Won!`
    : `Player 2 Won!`;
}

